# Indexing and Splicing with Strings

myString = "Hello World"

print(myString[0])
print(myString[8])
print(myString[9])
print(myString[-2])

letters = "abcdefghijklmnopqrstuvwxyz"
print(letters)
print(letters[2:])
print(letters[:4])
print(letters[3:10])

# Use string slicing to grab the word 'ink'  from inside 'tinker' 
